package com.gaps.hairengine;

public class HPoint 
{
	public float x = 0.0f;
	public float y = 0.0f;
	public float getX() {
		return x;
	}
	public void setX(float x) {
		this.x = x;
	}
	public float getY() {
		return y;
	}
	public void setY(float y) {
		this.y = y;
	}
	@Override
	public String toString() {
		return "HPoint [x=" + x + ", y=" + y + "]";
	}
	
	
	
}
